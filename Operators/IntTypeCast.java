class IntTypeCast 
{
	public static void main(String[] args) 
	{
		int a = 123456;
		long e = a;
		float f = a;
		double g = a;
		System.out.println("Original Value int = " +a);
		System.out.println("Typecasted to long = "+e);
		System.out.println("Typecasted to float = "+f);
		System.out.println("Typecasted to double = "+g);
	}
}
