class DoubleNarrow 
{
	public static void main(String[] args) 
	{
		double a = 100.000;
		int b = (int) a;
		float c = (float) a;
		long d = (long) a;
		char e = (char) a;
		short f = (short) a;
		byte g = (byte) a;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}
}
