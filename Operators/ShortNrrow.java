class ShortNrrow 
{
	public static void main(String[] args) 
	{
		float a = 143.56f;
		byte b = (byte) a;
		short c = (short) a;
		char d = (char) a;
		int e = (int) a;
		long f = (long) a;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
 }
}